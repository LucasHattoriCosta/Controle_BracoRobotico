# Alocacao de polos
import numpy as np

def PID_AP():
    # Kd, Ki, Kp
    A = np.array(
    [[7.281,0,0], # s^5
    [0.0048, 0, 7.281], # s^4
    [238.1, 7.281, 0.0048], # s^3
    [1.32*(10**-10),0.0048,238.1], # s^2
    [0,238.1,1.32*(10**-10)], # s^1
    [0,1.32*(10**-10),0]] # s^0
    )

    B = np.array([
    [-1 + 1], # s^5
    [-0.0003   + 49.5], # s^4
    [-68.78  + 1022.75], # s^3
    [-0.01021  + 11247.375], # s^2
    [-1168   + 66307.5], # s^1
    [-0 + 165923.4375]]) # s^0

    [KD, KI, KP] = np.linalg.lstsq(A,B, rcond=None)[0]

    return KD, KI, KP